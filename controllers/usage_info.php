                    수집목적 및 이용목적, 제3자에 대한 정보제공 관련사항(제공받은 자, 제공목적 및 제공할 정보의 내용) 등 『정보통신망 이용촉진 및 정보보호 등에 관한 법률』
                    에서 규정한 사항을 미리 명시하거나 고지해야
                    하며 <br>회원은 언제든지 이 동의를 철회할 수 있습니다.

