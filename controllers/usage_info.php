                    <br>1. 회원이 회사가 제공하는 서비스를 이용하지 아니하고 판매자와 직접 거래하는 행위(이하 “직거래”) 또는 이를 유도하는 행위

                    <br>2. 회사 또는 판매자의 판매 활동을 방해하거나 온라인 플랫폼을 이용하여 부당한 이득을 취하는 등 통상적인 전자상거래 관행에 부합하지 않는 거래상의
                    부정 행위

                    <br>3. 상품의 판매 또는 용역의 제공을 가장한 현금 융통 등 『여신전문금융업법』, 『정보통신망 이용촉진 및 정보보호 등에 관한 법률』 및 기타 관계
                    법령에 위배되거나 비정상적인 결제 행위

                    <br>4. 회사가 제공하는 서비스 이용방법에 의하지 아니하고 비정상적인 방법으로 서비스를 이용하거나 회사의 정보처리시스템에 접근하는 행위

                    <br>5. 회사가 제공하는 서비스와 관련 없거나 비합리적인 요청, 공서양속에 반하는 행위 등으로 회사의 업무를 방해하는 행위

                    <br>6. 기타 위 각 호에 준하여 회사 또는 제3자의 권리를 침해 또는 침해할 우려가 있거나, 전자상거래 질서를 위협하는 행위

                    <br>③ 회사는 제2항을 위반한 회원에 대하여 계약 해제, 회원 자격의 제한, 현금 환불 보류 등의 조치 및 민∙형사 상의 법적 조치 등을 취할 수
                    있습니다.

                    <br>④ 본 조 제2항, 제3항의 구체적인 내용은 “마켓오브메테리얼 서비스 이용 정책”에서 정합니다.

