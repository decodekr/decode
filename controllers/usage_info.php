                    제한되는 서비스를 구체적으로 명시하고, 필수수집항목이 아닌 개인정보의 수집·이용·제공에 관한 회원의 동의 거절을 이유로 회원가입 등 서비스 제공을 제한하거나
                    거절하지 않습니다.

                    <br>⑩ 회사의 온라인 플랫폼(제2조 제1호에서 정한 범위 내) 이외의 링크된 온라인 플랫폼에서는 회사의 개인정보처리방침이 적용되지 않습니다. 링크된 온라인
                    플랫폼 및 상품 등을 제공하는 제3자의 개인정보 취급과 관련하여서는 해당 온라인 플랫폼 및 제3자의 개인정보처리방침을 확인할 책임이 회원에게 있으며, 회사는
